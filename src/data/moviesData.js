// Utility functions
export const getUniqueGenres = () => {
    const genres = moviesData.map(movie => movie.genre);
    return [...new Set(genres)];
};

export const getUniqueYears = () => {
    const years = moviesData.map(movie => movie.year);
    return [...new Set(years)].sort((a, b) => b - a);
};

export const filterMovies = (searchTerm = '', genre = '', year = '', minRating = '') => {
    return moviesData.filter(movie => {
        const matchesSearch = movie.title.toLowerCase().includes(searchTerm.toLowerCase());
        const matchesGenre = !genre || movie.genre === genre;
        const matchesYear = !year || movie.year.toString() === year;
        const matchesRating = !minRating || movie.rating >= parseFloat(minRating);
        
        return matchesSearch && matchesGenre && matchesYear && matchesRating;
    });
};

export const getMovieById = (id) => {
    return moviesData.find(movie => movie.id === parseInt(id));
};

export const getTopRatedMovies = (limit = 10) => {
    return moviesData
        .sort((a, b) => b.rating - a.rating)
        .slice(0, limit);
};

export const getRecentMovies = (limit = 10) => {
    return moviesData
        .sort((a, b) => b.year - a.year)
        .slice(0, limit);
};